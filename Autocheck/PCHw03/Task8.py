def cost_delivery(quantity, *_, discount=0):
	""" Функція підраховує скільки коштує замовлення товару.

	Дякую за увагу. Для нас важливі лише два параметри - перший у якому вказана загальна кількість товарів і останній у якому вказано знижку."""
	result = (5 + 2 * (quantity - 1)) * (1 - discount)
	return result
